    @Override
    public void update(Observable o, Object arg) {
        if (o instanceof TSPModel_PtiDeb) {
            TSPModel_PtiDeb model = (TSPModel_PtiDeb) o;
            int id = model.getSegmentID();
            int p1 = model.getSegmentP1();
            int p2 = model.getSegmentP2();
            switch (model.getAction()) {
                case Add:
                    addToGraph(id, p1, p2);
                    break;
                case Remove:
                    removeFromGraph(id);
                    break;
                case NewBest:
                    updateBest();
                    break;
                case Finish:
                    finish();
                    break;
            }
            try {
                Thread.sleep(wait);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
    
     public void addToGraph(int id, int p1, int p2) {
        this.current.addEdge(Integer.toString(id), Integer.toString(p1), Integer.toString(p2));
    }

    public void removeFromGraph(int id) {
        this.current.removeEdge(Integer.toString(id));
    }

    public void updateBest() {
        while(this.best.getEdgeCount() > 0) {
            this.best.removeEdge(0);
        }
        for (Edge edge : this.current.getEachEdge()) {
            this.best.addEdge(edge.getId(), edge.getSourceNode().getId(), edge.getTargetNode().getId());
        }
    }

    public void finish() {
        System.out.println("Fini");
        JOptionPane.showMessageDialog(null,"Fini");
    }
    
